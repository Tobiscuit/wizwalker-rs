/// 3D position in game world coordinates
#[derive(Debug, Clone, Copy, Default, PartialEq)]
pub struct XYZ {
    pub x: f32,
    pub y: f32,
    pub z: f32,
}

/// Camera / entity orientation angles
#[derive(Debug, Clone, Copy, Default, PartialEq)]
pub struct Orient {
    pub pitch: f32,
    pub roll: f32,
    pub yaw: f32,
}

/// RGBA color
#[derive(Debug, Clone, Copy, Default, PartialEq)]
pub struct Color {
    pub r: u8,
    pub g: u8,
    pub b: u8,
    pub a: u8,
}

